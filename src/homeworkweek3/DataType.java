package homeworkweek3;

public class DataType {
    //practice of datatype initial value
    public static void main(String[] args) {
        byte b = -126; //testing of type value -128 to 127
        short s = -323; //initial value of short -32768 to 32767
        int i = 564546123; //initial value of int upto 9 digit
        long l = 111111111111111111L; // long nos define ending with capital L
        char c = 'J';//only one charactor must be written within ' '
        boolean o =true; //only allowing true or false so its conditional datatype
        float f = 0.000000000000025F; //any decimal value must written at the end with F or f
        double d = 45.000000000514; //any decimal value
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(c);
        System.out.println(o);
        System.out.println(f);
        System.out.println(d);




    }
}
